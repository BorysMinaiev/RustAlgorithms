#[allow(dead_code)]
struct Random {
    state: usize,
}

impl Random {
    fn next(&mut self) -> usize {
        let mut x = self.state;
        x ^= x << 13;
        x ^= x >> 7;
        x ^= x << 17;
        self.state = x;
        x
    }

    #[allow(dead_code)]
    fn next_in_range(&mut self, from: usize, to: usize) -> usize {
        assert!(from < to);
        from + self.next() % (to - from)
    }

    #[allow(dead_code)]
    fn next_double(&mut self) -> f64 {
        (self.next() as f64) / (std::usize::MAX as f64)
    }

    #[allow(dead_code)]
    fn new(seed: usize) -> Self {
        assert_ne!(seed, 0);
        Self {
            state: seed,
        }
    }
}

pub fn main() {
    let stdout = io::stdout();
    let mut out = std::io::BufWriter::new(stdout.lock());
    let mut sc = Scanner::new();

    let mut rnd = Random::new(787788);

    const MAX_K: usize = 1_000_001;
    let mut seen = vec![false; MAX_K];

    const MAX_V: usize = 50;
    for _ in 0..1_000_000 {
        let max_v = rnd.next_in_range(1, MAX_V);
        let n = 1 + rnd.next_in_range(0, 8);
        let mut a = vec![0; n];
        for i in 0..n {
            a[i] = rnd.next_in_range(0, max_v * 2) as i32 - (max_v as i32);
        }
        let mut cnt0 = 0;
        for mask in 0..1 << n {
            let mut sum = 0;
            for i in 0..n {
                if (1 << i) & mask != 0 {
                    sum += a[i];
                }
            }
            if sum == 0 {
                cnt0 += 1;
            }
        }
        if cnt0 >= MAX_K {
            continue;
        }
        if !seen[cnt0] {
            seen[cnt0] = true;
            dbg!("seen", cnt0);
        }
    }

    for i in 0..100 {
        if !seen[i] {
            dbg!("not OK ", i);
        }
    }
}
