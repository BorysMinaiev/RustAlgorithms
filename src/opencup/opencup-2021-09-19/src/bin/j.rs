mod modulo {
    const MODULO: i32 = 998244353;

    #[derive(Copy, Clone, Eq, PartialEq)]
    pub struct Mod(i32);

    impl Mod {
        #[allow(unused)]
        pub const ZERO: Self = Self(0);

        #[allow(unused)]
        pub const ONE: Self = Self(1);

        fn rev_rec(a: i32, m: i32) -> i32 {
            if a == 1 {
                return a;
            }
            return ((1 - Self::rev_rec(m % a, a) as i64 * m as i64) / a as i64 + m as i64) as i32;
        }

        #[allow(dead_code)]
        fn inv(self) -> Mod {
            Mod(Self::rev_rec(self.0, MODULO))
        }

        #[allow(dead_code)]
        pub fn new(mut x: i32) -> Self {
            if x < 0 {
                x += MODULO;
            } else if x >= MODULO {
                x -= MODULO;
            }
            assert!(0 <= x && x < MODULO);
            Self(x)
        }
    }

    impl std::fmt::Display for Mod {
        fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
            write!(f, "{}", self.0)
        }
    }

    impl std::fmt::Debug for Mod {
        fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
            const MAX: usize = 100;
            if self.0 <= MAX as i32 {
                write!(f, "{}", self.0)
            } else if self.0 >= MODULO - MAX as i32 {
                write!(f, "-{}", MODULO - self.0)
            } else {
                for denum in 1..MAX {
                    for num in 1..MAX {
                        if Mod(num as i32) / Mod(denum as i32) == *self {
                            return write!(f, "{}/{}", num, denum);
                        }
                    }
                }
                write!(f, "(?? {} ??)", self.0)
            }
        }
    }

    impl std::ops::Add for Mod {
        type Output = Mod;

        fn add(self, rhs: Self) -> Self::Output {
            let res = self.0 + rhs.0;
            if res >= MODULO {
                Mod(res - MODULO)
            } else {
                Mod(res)
            }
        }
    }

    impl std::ops::AddAssign for Mod {
        fn add_assign(&mut self, rhs: Self) {
            self.0 += rhs.0;
            if self.0 >= MODULO {
                self.0 -= MODULO;
            }
        }
    }

    impl std::ops::Sub for Mod {
        type Output = Mod;

        fn sub(self, rhs: Self) -> Self::Output {
            let res = self.0 - rhs.0;
            if res < 0 {
                Mod(res + MODULO)
            } else {
                Mod(res)
            }
        }
    }

    impl std::ops::SubAssign for Mod {
        fn sub_assign(&mut self, rhs: Self) {
            self.0 -= rhs.0;
            if self.0 < 0 {
                self.0 += MODULO;
            }
        }
    }

    impl std::ops::Mul for Mod {
        type Output = Mod;

        fn mul(self, rhs: Self) -> Self::Output {
            let res = (self.0 as i64) * (rhs.0 as i64) % (MODULO as i64);
            Mod(res as i32)
        }
    }

    impl std::ops::MulAssign for Mod {
        fn mul_assign(&mut self, rhs: Self) {
            self.0 = ((self.0 as i64) * (rhs.0 as i64) % (MODULO as i64)) as i32;
        }
    }

    impl std::ops::Div for Mod {
        type Output = Mod;

        fn div(self, rhs: Self) -> Self::Output {
            let rhs_inv = rhs.inv();
            self * rhs_inv
        }
    }

    impl std::ops::DivAssign for Mod {
        fn div_assign(&mut self, rhs: Self) {
            *self *= rhs.inv();
        }
    }
}

use modulo::*;

pub fn main() {
    let stdout = io::stdout();
    let mut out = std::io::BufWriter::new(stdout.lock());
    let mut sc = Scanner::new();

    let n = sc.usize();
    let mut where_p = vec![0; n];
    for pos in 0..n {
        where_p[sc.usize() - 1] = pos;
    }
    let q_from_input = sc.vec::<usize>(n);

    let mut q = vec![];
    for i in 0..n {
        q.push(q_from_input[where_p[i]]);
    }

    // [max_pos + 1][counter_free]
    let mut dp = vec![vec![Mod::ZERO; n + 1]; n + 1];
    let mut next_dp = vec![vec![Mod::ZERO; n + 1]; n + 1];
    dp[0][0] = Mod::ONE;
    for value in 1..=n {
        for i in 0..=n {
            for j in 0..=n {
                next_dp[i][j] = Mod::ZERO;
            }
        }

        let mut where_pos = n;
        for pos in 0..n {
            if q[pos] == value {
                where_pos = pos;
            }
        }

        for max_pos_p1 in 0..=n {
            for counter in 0..=n {
                let cur_dp = dp[max_pos_p1][counter];
                if cur_dp == Mod::ZERO {
                    continue;
                }
                if where_pos == n {
                    next_dp[max_pos_p1][counter + 1] += cur_dp;
                    for next_pos in max_pos_p1..n {
                        if q[next_pos] == 0 {
                            next_dp[next_pos + 1][counter] += cur_dp;
                        }
                    }
                } else {
                    next_dp[max_pos_p1][counter] += cur_dp;
                    if where_pos >= max_pos_p1 {
                        next_dp[where_pos + 1][counter] += cur_dp;
                    }
                }
            }
        }


        for i in 0..=n {
            for j in 0..=n {
                dp[i][j] = next_dp[i][j];
            }
        }
    }

    let mut res = Mod::ZERO;
    for max_pos in 0..=n {
        let mut fact = Mod::ONE;
        for counter in 0..=n {
            if counter != 0 {
                fact *= Mod::new(counter as i32);
            }
            res += dp[max_pos][counter] * fact;
        }
    }
    writeln!(out, "{}", res).unwrap();
}
